function matSetInitializeCharacteristicMatrix( obj, meshUnion )
%
obj.PNPS = mxFirstOrderDerivAboutNohydroPressInVert( meshUnion.cell.Np,...
    meshUnion.K, meshUnion.Nz, meshUnion.mesh2d.K, meshUnion.cell.Nface,...
    meshUnion.EToE, meshUnion.tz, meshUnion.cell.Dt, meshUnion.J, meshUnion.mesh2d.J,...
    meshUnion.mesh2d.cell.M, meshUnion.cell.M, meshUnion.cell.Fmask, meshUnion.cell.Nfp, ...
    meshUnion.BottomEdge.FToE, meshUnion.BottomEdge.FToN1, meshUnion.BottomEdge.FToN2);

obj.PNPX = mxFirstOrderDerivAboutNohydroPressInHorizontal( meshUnion.cell.Np,...
    meshUnion.K, meshUnion.Nz, meshUnion.mesh2d.K, meshUnion.cell.Nface,...
    meshUnion.EToE, meshUnion.InnerEdge.FToE, meshUnion.InnerEdge.FToF,...
    meshUnion.InnerEdge.FToN1, meshUnion.InnerEdge.FToN2, meshUnion.J, ...
    meshUnion.InnerEdge.Js, meshUnion.cell.M, meshUnion.InnerEdge.M, ...
    meshUnion.InnerEdge.Ne, meshUnion.cell.Nfp, meshUnion.InnerEdge.nx,...
    meshUnion.rx, meshUnion.sx, meshUnion.cell.Dr, meshUnion.cell.Ds,...
    meshUnion.cell.Fmask, obj.BoundaryEdge, int8(obj.BoundaryEdge.ftype),...
    obj.BoundaryEdge.nx);

obj.PNPY = mxFirstOrderDerivAboutNohydroPressInHorizontal( meshUnion.cell.Np,...
    meshUnion.K, meshUnion.Nz, meshUnion.mesh2d.K, meshUnion.cell.Nface,...
    meshUnion.EToE, meshUnion.InnerEdge.FToE, meshUnion.InnerEdge.FToF,...
    meshUnion.InnerEdge.FToN1, meshUnion.InnerEdge.FToN2, meshUnion.J, ...
    meshUnion.InnerEdge.Js, meshUnion.cell.M, meshUnion.InnerEdge.M, ...
    meshUnion.InnerEdge.Ne, meshUnion.cell.Nfp, meshUnion.InnerEdge.ny,...
    meshUnion.ry, meshUnion.sy, meshUnion.cell.Dr, meshUnion.cell.Ds,...
    meshUnion.cell.Fmask, obj.BoundaryEdge, int8(obj.BoundaryEdge.ftype),...
    obj.BoundaryEdge.ny);


end